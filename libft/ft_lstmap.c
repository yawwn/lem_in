#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list *new_list;
	t_list *new_elem;
	t_list *prev_elem;

	new_list = NULL;
	if (lst && (*f))
	{
		new_list = (*f)(lst);
		prev_elem = new_list;
		lst = lst->next;
		while (lst)
		{
			new_elem = (*f)(lst);
			prev_elem->next = new_elem;
			prev_elem = new_elem;
			lst = lst->next;
		}
		prev_elem->next = NULL;
	}
	return (new_list);
}
